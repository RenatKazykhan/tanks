    getMapKey(stageId) {
        // Этапы типа "Лабиринт": 2, 7, 12, 17, 22
        if (stageId === 2)  return 'level2';
        if (stageId === 7)  return 'level7';
        if (stageId === 12) return 'level12';
        if (stageId === 17) return 'level17';
        if (stageId === 22) return 'level22';
        // Запасной вариант
        return 'level2';
    }

    // Method to validate map layout (useful for debugging)
    validateMap(mapKey) {
        const layout = this.maps[mapKey];
        if (!layout) return false;

        const heights = layout.map(row => row.length);
        const maxWidth = Math.max(...heights);

        return layout.every(row => row.length === maxWidth);
    }
